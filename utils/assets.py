def get_rune_asset(level: int, rune_type: str, team: bool) -> str:
    """Get rune asset filename dynamically: rune{level}{type}{s/t}.png"""
    suffix = "t" if team else "s"
    return f"rune{level}{rune_type.lower()}{suffix}.png"

